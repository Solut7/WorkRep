package com.example.demo;


public class ClientsInfo {
     Integer CurrentAmountClients;
     Integer NewClientsNumber;
     Integer LeftClientsNumber;
     Integer ActiveClientsNumber;
     Integer Visitors;
     Integer ConvertedUsers;
     Integer CustomerLifetime;

    public Integer getCurrentAmountClients() {
        return CurrentAmountClients;
    }

    public void setCurrentAmountClients(Integer currentAmountClients) {
        CurrentAmountClients = currentAmountClients;
    }

    public Integer getNewClientsNumber() {
        return NewClientsNumber;
    }

    public void setNewClientsNumber(Integer newClientsNumber) {
        NewClientsNumber = newClientsNumber;
    }

    public Integer getLeftClientsNumber() {
        return LeftClientsNumber;
    }

    public void setLeftClientsNumber(Integer leftClientsNumber) {
        LeftClientsNumber = leftClientsNumber;
    }

    public Integer getActiveClientsNumber() {
        return ActiveClientsNumber;
    }

    public void setActiveClientsNumber(Integer activeClientsNumber) {
        ActiveClientsNumber = activeClientsNumber;
    }


    public Integer getVisitors() {
        return Visitors;
    }

    public void setVisitors(Integer visitors) {
        Visitors = visitors;
    }

    public Integer getConvertedUsers() {
        return ConvertedUsers;
    }

    public void setConvertedUsers(Integer convertedUsers) {
        ConvertedUsers = convertedUsers;
    }

    public Integer getCustomerLifetime() {
        return CustomerLifetime;
    }

    public void setCustomerLifetime(Integer customerLifetime) {
        CustomerLifetime = customerLifetime;
    }

}
