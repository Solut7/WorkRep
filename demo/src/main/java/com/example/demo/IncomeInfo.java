package com.example.demo;

public class IncomeInfo {
     float AllIncome;
     float InAPP;
     float SubInc;
     float SellInc;

    public float getInAPP() {
        return InAPP;
    }
    public void setInAPP(float inAPP) {
        InAPP = inAPP;
    }
    public float getSubInc() {
        return SubInc;
    }
    public void setSubInc(float subInc) {
        SubInc = subInc;
    }
    public float getSellInc() {
        return SellInc;
    }
    public void setSellInc(float sellInc) {
        SellInc = sellInc;
    }
    public float getAllIncome() {
        return AllIncome;
    }

    public void setAllIncome(float allIncome) {
        AllIncome = allIncome;
    }
}
