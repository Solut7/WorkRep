package com.example.demo;

public class ExpensesInfo {
     float MarketingExpenses;
     float SaleExpenses;
     float DevelopExpenses;
     float PrototypeExpenses;
     float AllExpenses;

    public float getMarketingExpenses() {
        return MarketingExpenses;
    }

    public void setMarketingExpenses(float marketingExpenses) {
        MarketingExpenses = marketingExpenses;
    }

    public float getSaleExpenses() {
        return SaleExpenses;
    }

    public void setSaleExpenses(float saleExpenses) {
        SaleExpenses = saleExpenses;
    }

    public float getDevelopExpenses() {
        return DevelopExpenses;
    }

    public void setDevelopExpenses(float developExpenses) {
        DevelopExpenses = developExpenses;
    }

    public float getPrototypeExpenses() {
        return PrototypeExpenses;
    }

    public void setPrototypeExpenses(float prototypeExpenses) {
        PrototypeExpenses = prototypeExpenses;
    }

    public float getAllExpenses() {
        return AllExpenses;
    }

    public void setAllExpenses(float allExpenses) {
        AllExpenses = allExpenses;
    }


}
